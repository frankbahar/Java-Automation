package com.class01;

public class DataTypes {

	public static void main(String[] args) {
		// creating variables
		// numbers
		byte b=-128;
		short s=5546;
		int i=6789;
		long l=5545454545465656l;
		//number with decimals
		float f=2.2f;
		double d=3.76866788;
		//character
		char c='A';
		// boolean true or false
		boolean g = false;
		//String
		String str = "Hello123%$#";
		System.out.println(b+  " " + s + " " + i + " " + l + " " + f + " " + 
				d + " " + c + " " + g + " " + str);
	
	}

}
